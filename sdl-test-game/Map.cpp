Map::Map()
{
	sizex = 30; sizey = 30;
	map = new int[sizex * sizey];
	memcpy(map, map1, 30 * 30 * sizeof(int));
	//for (int i = 0; i < sizex * sizey; i++) map[i] = 0;
	//for (int i = 4; i < sizex * sizey; i+=9) map[i] = (int)Tile::Stone;
	tiles.insert(std::pair <Tile, Sprite*>(Tile::Gray, new Sprite("imgs/tileg.png", TILE_WIDTH_HALF, 0)));
	tiles.insert(std::pair <Tile, Sprite*>(Tile::Red, new Sprite("imgs/tiler.png", TILE_WIDTH_HALF, 0)));
	tiles.insert(std::pair <Tile, Sprite*>(Tile::Stone, new Sprite("imgs/tile_block.png", TILE_WIDTH_HALF, 100)));
	tiles.insert(std::pair <Tile, Sprite*>(Tile::Flag, new Sprite("imgs/flag.png", TILE_WIDTH_HALF, 100)));
	
}

void Map::changeBlock(SDL_Point log)
{
	if ((log.x >= 0 && log.x < sizex) &&
		(log.y >= 0 && log.y < sizey))
	{
		if (map[log.x + log.y*sizex] == (int)Tile::Gray) map[log.x + log.y*sizex] = (int)Tile::Stone;
		else map[log.x + log.y*sizex] = (int)Tile::Gray;
	}
}

Map::~Map()
{
	delete map;
	for (auto it = tiles.begin(); it != tiles.end(); ++it)
	{
		delete it->second;
	}
}
